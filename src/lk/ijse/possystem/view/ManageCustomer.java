    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            boolean res = CustomerController.addCustomer(
            new CustomerDTO(
                    txtCustomerID.getText(),
                    txtCustomerName.getText(),
                    txtCustomerAddress.getText(), 
                    Double.parseDouble(txtCustomerSalary.getText())
            ));
            if (res) {
                JOptionPane.showMessageDialog(this, "Save Customer");
                loadAllCustomer();
            } else {
                JOptionPane.showMessageDialog(this, "Not Added Customer");
            }
        } catch (Exception ex) {
            Logger.getLogger(ManageCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tbnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbnSearchActionPerformed
        try {
            CustomerDTO seCust = CustomerController.searchCustomer(txtCustomerID.getText());
            if (seCust != null) {
                txtCustomerID.setText(seCust.getCid());
                txtCustomerName.setText(seCust.getName());
                txtCustomerAddress.setText(seCust.getAddress());
                txtCustomerSalary.setText(""+seCust.getSalary());
            } else {
                JOptionPane.showMessageDialog(this,
                        "Not Found Customer");
            }
        } catch (Exception ex) {
            Logger.getLogger(ManageCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tbnSearchActionPerformed

